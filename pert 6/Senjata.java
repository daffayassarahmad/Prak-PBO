class Senjata {
    private String nama;
    private int dayaSerang;

    public Senjata(String nama, int dayaSerang) {
        this.nama = nama;
        this.dayaSerang = dayaSerang;
    }

    public String getNama() {
        return this.nama;
    }


    public int getDayaSerang() {
        return this.dayaSerang;
    }
}